    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jButton1.isEnabled() && (!jTextField1.getText().isEmpty() &&
                !jTextField2.getText().isEmpty() &&
                !jTextField3.getText().isEmpty() &&
                !jTextField4.getText().isEmpty() &&
                !jTextField5.getText().isEmpty() &&
                !jTextField6.getText().isEmpty())){

                Empleado empleado = new Empleado();
                empleado.setPnombre(jTextField1.getText());
                empleado.setSnombre(jTextField2.getText());
                empleado.setPapellido(jTextField3.getText());
                empleado.setSapellido(jTextField4.getText());
                empleado.setIdentificacion(jTextField5.getText());
                empleado.setDireccion(jTextField6.getText());

                bd2.Empleado.insertarEmpleado(new ConexionMongo(), empleado);
                JOptionPane.showMessageDialog(null,"Se ha guardado exitosamente", "Guardar", JOptionPane.INFORMATION_MESSAGE);
                limpiar();
            }else{
            JOptionPane.showMessageDialog(null,"Por favor rellene todos los campos", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed


