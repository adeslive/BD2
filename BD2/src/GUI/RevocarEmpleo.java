    private void Cargar(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_Cargar
        if(!idCli.getText().isEmpty()){
            
            info.stream().map((linea) -> {
                Vector<String> temp = new Vector<>();
                String[] l_temp = linea.split(",");
                temp.addAll(Arrays.asList(l_temp));
                return temp;
            }).forEach((temp) -> {
                objetos.add(temp);
            });

            objetos.stream().forEach((vec) -> {
                dtm.addRow(vec);
            });
        }
    }//GEN-LAST:event_Cargar

